function escapeHtml(v) {
  return String(v ?? '')
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#39;');
}

function sourceBlock(source) {
  const label = CONF_LABEL[source.confidence] || '[NOT VERIFIED]';
  if (source.status === SOURCE_STATUS.FOUND) {
    return `${label} FOUND — ${source.sourceLabel}`;
  }
  if (source.status === SOURCE_STATUS.PARTIAL) {
    return `${label} PARTIAL RESULT — ${source.sourceLabel}. ${source.errorDetail || 'Extraction incomplete — format changed.'}`;
  }
  if (source.status === SOURCE_STATUS.NOT_FOUND) {
    return `[NOT FOUND] NOT FOUND — ${source.sourceLabel} returned no responsive results for this query.`;
  }
  if (source.status === SOURCE_STATUS.UNAVAILABLE || source.status === SOURCE_STATUS.BLOCKED) {
    return `[SOURCE UNAVAILABLE] SOURCE UNAVAILABLE — ${source.sourceLabel} could not be accessed. ${source.errorDetail || 'No detail provided.'}. Manual review recommended.`;
  }
  return `[MANUAL REVIEW REQUIRED] RETRIEVAL ERROR — ${source.sourceLabel} encountered an error: ${source.errorDetail || 'Unknown error'}.`;
}

export function buildDynamicReportFromWorkflow(workflow, order) {
  const generatedAt = new Date().toISOString();
  const disclaimerBase = DISCLAIMERS[workflow.tier] || DISCLAIMERS.comprehensive;
  const disclaimer = `${disclaimerBase}\n\nReport generated: ${generatedAt} | Order ID: ${workflow.orderId}`;

  const sections = (workflow.sources || []).map((source) => ({
    title: source.sourceLabel,
    finding: sourceBlock(source),
    source
  }));

  const report = {
    reportType: workflow.tier,
    orderId: workflow.orderId,
    generatedAt,
    customerName: order.customerName,
    customerEmail: order.customerEmail,
    purchasedTier: order.purchased_tier,
    customerInputs: workflow.inputs,
    overallStatus: workflow.overallStatus,
    sections,
    discrepancy: workflow.discrepancy || null,
    confidenceMatrix: workflow.confidenceMatrix || null,
    nextSteps: workflow.nextSteps || null,
    sources: workflow.sources || [],
    disclaimer
  };

  return report;
}

export function dynamicReportToText(report) {
  const lines = [
    `TraceWorks ${report.reportType} Report`,
    `Order ID: ${report.orderId}`,
    `Generated: ${report.generatedAt}`,
    `Overall Status: ${report.overallStatus}`,
    `Customer: ${report.customerName} <${report.customerEmail}>`,
    `Inputs: ${JSON.stringify(report.customerInputs)}`,
    ''
  ];

  for (const section of report.sections) {
    lines.push(`## ${section.title}`);
    lines.push(`- ${section.finding}`);
    lines.push(`- Source URL: ${section.source.sourceUrl}`);
    lines.push(`- Query Used: ${section.source.queryUsed}`);
    lines.push(`- Queried At: ${section.source.queriedAt}`);
    if (section.source.data) lines.push(`- Data: ${JSON.stringify(section.source.data)}`);
    lines.push('');
  }

  if (report.nextSteps?.recommendations?.length) {
    lines.push('## Recommended Next Steps');
    for (const step of report.nextSteps.recommendations) lines.push(`- ${step}`);
    lines.push(report.nextSteps.disclaimer);
    lines.push('');
  }

  lines.push('## Disclaimer');
  lines.push(report.disclaimer);
  return lines.join('\n');
}

