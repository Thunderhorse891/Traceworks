import { mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import {
  buildWixLandingElement,
  buildWixPreviewHtml,
  DEFAULT_WIX_APP_BASE_URL,
  WIX_WIDGET_TAG_NAME,
} from './_lib/wix-custom-element.mjs';

function parseArgs(argv) {
  const args = { appUrl: DEFAULT_WIX_APP_BASE_URL };
  for (let i = 0; i < argv.length; i += 1) {
    const token = argv[i];
    if (token === '--app-url' && argv[i + 1]) {
      args.appUrl = argv[i + 1];
      i += 1;
    }
  }
  return args;
}

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, '..');
const outputDir = path.join(rootDir, 'output', 'wix');

const { appUrl } = parseArgs(process.argv.slice(2));

await mkdir(outputDir, { recursive: true });

const elementPath = path.join(outputDir, 'traceworks-landing.element.js');
const previewPath = path.join(outputDir, 'traceworks-landing.preview.html');
const readmePath = path.join(outputDir, 'README.md');

await writeFile(elementPath, buildWixLandingElement({ appBaseUrl: appUrl }), 'utf8');
await writeFile(previewPath, buildWixPreviewHtml({ appBaseUrl: appUrl }), 'utf8');
await writeFile(
  readmePath,
  `# TraceWorks Wix Output

Generated by \`npm run build:wix\`.

- Custom element tag: \`${WIX_WIDGET_TAG_NAME}\`
- JavaScript bundle: \`traceworks-landing.element.js\`
- Local preview file: \`traceworks-landing.preview.html\`
- Default app base URL: \`${appUrl}\`

Host the JavaScript bundle over HTTPS, then register a Custom Element in Wix Studio using the tag name above.
`,
  'utf8'
);

console.log(`Wix build complete:
- ${elementPath}
- ${previewPath}
- ${readmePath}`);
