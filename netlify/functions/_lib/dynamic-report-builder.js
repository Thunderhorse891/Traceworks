function escapeHtml(value) {
  return String(value ?? '')
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#39;');
}

function sourceNarrative(source) {
  const label = CONF_LABEL[source.confidence] || '[NOT VERIFIED]';
  if (source.status === SOURCE_STATUS.FOUND) {
    return `${label} Found responsive records in ${source.sourceLabel}.`;
  }
  if (source.status === SOURCE_STATUS.PARTIAL) {
    return `${label} Partial extraction from ${source.sourceLabel}. ${source.errorDetail || 'Automation reached the source but the result shape was incomplete.'}`;
  }
  if (source.status === SOURCE_STATUS.SKIPPED) {
    return `[SKIPPED] ${source.sourceLabel} was not queried because the order did not include the identifier type this source requires.`;
  }
  if (source.status === SOURCE_STATUS.NOT_FOUND) {
    return `[NOT FOUND] ${source.sourceLabel} returned no responsive result for the query issued.`;
  }
  if (source.status === SOURCE_STATUS.UNAVAILABLE || source.status === SOURCE_STATUS.BLOCKED) {
    return `[SOURCE UNAVAILABLE] ${source.sourceLabel} could not be accessed automatically. ${source.errorDetail || 'Manual review is required.'}`;
  }
  return `[MANUAL REVIEW REQUIRED] ${source.sourceLabel} returned an error. ${source.errorDetail || 'Unknown retrieval error.'}`;
}

function summarizeSources(sources = []) {
  return sources.reduce(
    (summary, source) => {
      summary.total += 1;
      if (source.status === SOURCE_STATUS.FOUND) summary.found += 1;
      else if (source.status === SOURCE_STATUS.NOT_FOUND) summary.notFound += 1;
      else if (source.status === SOURCE_STATUS.SKIPPED) summary.skipped += 1;
      else if (source.status === SOURCE_STATUS.PARTIAL) summary.partial += 1;
      else if (source.status === SOURCE_STATUS.UNAVAILABLE || source.status === SOURCE_STATUS.BLOCKED) summary.manualReview += 1;
      else summary.errors += 1;
      return summary;
    },
    { total: 0, found: 0, notFound: 0, skipped: 0, partial: 0, manualReview: 0, errors: 0 }
  );
}

function normalizeInputRows(inputs = {}) {
  return Object.entries(inputs)
    .filter(([, value]) => value != null && !(Array.isArray(value) && value.length === 0) && value !== '')
    .map(([key, value]) => ({
      label: key.replace(/([A-Z])/g, ' $1').replaceAll('_', ' ').replace(/^./, (char) => char.toUpperCase()),
      value: Array.isArray(value) ? value.join(', ') : String(value),
    }));
}

function buildKeyFindings(sections = []) {
  return sections
    .filter((section) => section.source.status === SOURCE_STATUS.FOUND)
    .slice(0, 5)
    .map((section) => `${section.source.sourceLabel}: ${section.finding}`);
}

function summarizeChainAnalysis(chainAnalysis) {
  if (!chainAnalysis) return null;
  const items = [
    `Continuity status: ${chainAnalysis.chainStatus || 'not_available'}`,
    `Instrument count reviewed: ${Array.isArray(chainAnalysis.chronologicalSequence) ? chainAnalysis.chronologicalSequence.length : 0}`,
    ...(Array.isArray(chainAnalysis.gaps) ? chainAnalysis.gaps : []),
    ...(Array.isArray(chainAnalysis.conflictFlags) ? chainAnalysis.conflictFlags : [])
  ].filter(Boolean);
  if (!items.length) return null;
  return { title: 'Chain Analysis', items };
}

function candidateLabel(candidate = {}, index = 0) {
  return candidate.name || candidate.fullName || candidate.personName || candidate.label || `Candidate ${index + 1}`;
}

function summarizeHeirCandidates(candidates = []) {
  if (!Array.isArray(candidates) || !candidates.length) return null;
  const items = candidates.slice(0, 8).map((candidate, index) => {
    const details = [];
    if (candidate.address) details.push(`Address: ${candidate.address}`);
    if (candidate.phone) details.push(`Phone: ${candidate.phone}`);
    if (candidate.email) details.push(`Email: ${candidate.email}`);
    const base = `${candidateLabel(candidate, index)} — ${candidate.label || 'unscored'} (score ${Number(candidate.score || 0)})`;
    return details.length ? `${base}. ${details.join('. ')}.` : base;
  });
  return { title: 'Heir Candidate Review', items };
}

function flattenPublicRecordGaps(publicRecords, prefix = '') {
  if (!publicRecords) return [];
  if (Array.isArray(publicRecords.gaps)) {
    return publicRecords.gaps.map((gap) => `${prefix}${gap}`);
  }

  return Object.entries(publicRecords)
    .flatMap(([key, value]) => flattenPublicRecordGaps(value, `${key}: `));
}

function summarizeCoverageGaps(workflow) {
  const items = [
    ...flattenPublicRecordGaps(workflow.publicRecords),
    ...(Array.isArray(workflow.partialReasons) ? workflow.partialReasons : []),
    ...(Array.isArray(workflow.failureReasons) ? workflow.failureReasons : [])
  ].filter(Boolean);

  if (!items.length) return null;
  return { title: 'Coverage Gaps', items: [...new Set(items)] };
}

function summarizeOsint(workflow) {
  const osint = workflow.osint;
  if (!osint) return null;
  const items = [];
  if (osint.providerNote) items.push(osint.providerNote);
  if (osint.coverage) {
    items.push(`Premium OSINT coverage: ${Number(osint.coverage.totalOpenWebSources || 0)} cited lead(s), ${Number(osint.coverage.providersWithHits || 0)} provider(s) with hits.`);
  }
  for (const source of (osint.sources || []).slice(0, 5)) {
    items.push(`${source.provider}: ${source.title} (${source.url})`);
  }
  if (!items.length) return null;
  return { title: 'Open-Web OSINT Enrichment', items };
}

function summarizeDiscrepancies(discrepancy) {
  if (!discrepancy) return null;
  const items = [
    ...(Array.isArray(discrepancy.conflicts) ? discrepancy.conflicts : []),
    ...(Array.isArray(discrepancy.unresolvedFlags) ? discrepancy.unresolvedFlags : [])
  ].filter(Boolean);
  if (!items.length) return null;
  return { title: 'Cross-Source Discrepancies', items };
}

function summarizeConfidenceMatrix(confidenceMatrix) {
  if (!confidenceMatrix) return null;
  const buckets = [
    ['Strongly supported', confidenceMatrix.stronglySupported],
    ['Moderately supported', confidenceMatrix.moderatelySupported],
    ['Weak or speculative', confidenceMatrix.weakOrSpeculative],
    ['Manual validation required', confidenceMatrix.requiresManualValidation]
  ];
  const items = buckets
    .map(([label, rows]) => `${label}: ${Array.isArray(rows) ? rows.length : 0}`)
    .filter(Boolean);
  if (!items.length) return null;
  return { title: 'Confidence Matrix', items };
}

function buildAnalysisPanels(workflow) {
  return [
    summarizeOsint(workflow),
    summarizeChainAnalysis(workflow.chainAnalysis),
    summarizeHeirCandidates(workflow.scoredCandidates),
    summarizeDiscrepancies(workflow.discrepancy),
    summarizeConfidenceMatrix(workflow.confidenceMatrix),
    summarizeCoverageGaps(workflow)
  ].filter(Boolean);
}

function statusHeadline(report) {
  if (report.overallStatus === 'complete') return 'Automated research completed successfully';
  if (report.overallStatus === 'partial') return 'Automated research completed with documented gaps';
  return 'Automated research encountered blocking issues';
}

function statusTone(report) {
  if (report.overallStatus === 'complete') return 'ok';
  if (report.overallStatus === 'partial') return 'warn';
  return 'error';
}

function humanizeLabel(value) {
  return String(value || '')
    .replaceAll('_', ' ')
    .replace(/\b\w/g, (char) => char.toUpperCase());
}

function formatTimestamp(value) {
  if (!value) return 'Not captured';
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return String(value);
  return date.toLocaleString('en-US', {
    dateStyle: 'medium',
    timeStyle: 'short'
  });
}

function sourceToneFromStatus(status) {
  if (status === SOURCE_STATUS.FOUND) return 'ok';
  if (status === SOURCE_STATUS.PARTIAL) return 'warn';
  if (status === SOURCE_STATUS.NOT_FOUND || status === SOURCE_STATUS.SKIPPED) return 'muted';
  return 'error';
}

function renderSourceUrl(url) {
  const value = String(url || '').trim();
  if (!value) return '<strong>Not captured</strong>';
  if (/^https?:\/\//i.test(value)) {
    return `<a href="${escapeHtml(value)}" target="_blank" rel="noreferrer noopener">${escapeHtml(value)}</a>`;
  }
  return `<strong>${escapeHtml(value)}</strong>`;
}

export function buildDynamicReportFromWorkflow(workflow, order) {
  const generatedAt = new Date().toISOString();
  const disclaimerBase = DISCLAIMERS[workflow.tier] || DISCLAIMERS.comprehensive;
  const disclaimer = `${disclaimerBase}\n\nReport generated: ${generatedAt} | Order ID: ${workflow.orderId}`;
  const sections = (workflow.sources || []).map((source) => ({
    title: source.sourceLabel,
    finding: sourceNarrative(source),
    source,
  }));
  const sourceSummary = summarizeSources(workflow.sources || []);
  const keyFindings = buildKeyFindings(sections);
  const analysisPanels = buildAnalysisPanels(workflow);

  return {
    reportType: workflow.tier,
    orderId: workflow.orderId,
    generatedAt,
    customerName: order.customerName,
    customerEmail: order.customerEmail,
    purchasedTier: order.purchased_tier,
    customerInputs: workflow.inputs,
    overallStatus: workflow.overallStatus,
    executiveSummary: statusHeadline({ overallStatus: workflow.overallStatus }),
    sourceSummary,
    keyFindings,
    analysisPanels,
    sections,
    discrepancy: workflow.discrepancy || null,
    confidenceMatrix: workflow.confidenceMatrix || null,
    nextSteps: workflow.nextSteps || null,
    sources: workflow.sources || [],
    disclaimer,
  };
}

export function dynamicReportToText(report) {
  const sourceSummary = report.sourceSummary || summarizeSources((report.sections || []).map((section) => section.source).filter(Boolean));
  const keyFindings = Array.isArray(report.keyFindings) ? report.keyFindings : [];
  const analysisPanels = Array.isArray(report.analysisPanels) ? report.analysisPanels : [];
  const inputRows = normalizeInputRows(report.customerInputs);
  const lines = [
    `TraceWorks ${report.reportType} Report`,
    `Order ID: ${report.orderId}`,
    `Generated: ${report.generatedAt}`,
    `Overall Status: ${report.overallStatus}`,
    `Executive Summary: ${report.executiveSummary}`,
    '',
    '## Source Summary',
    `- Sources Attempted: ${sourceSummary.total}`,
    `- Found: ${sourceSummary.found}`,
    `- Partial: ${sourceSummary.partial}`,
    `- Not Found: ${sourceSummary.notFound}`,
    `- Skipped: ${sourceSummary.skipped}`,
    `- Manual Review Required: ${sourceSummary.manualReview}`,
    `- Errors: ${sourceSummary.errors}`,
    '',
    '## Intake Profile',
  ];

  for (const row of inputRows) {
    lines.push(`- ${row.label}: ${row.value}`);
  }

  lines.push('');

  if (keyFindings.length) {
    lines.push('## Key Findings');
    for (const finding of keyFindings) lines.push(`- ${finding}`);
    lines.push('');
  }

  if (analysisPanels.length) {
    lines.push('## Investigative Analysis');
    for (const panel of analysisPanels) {
      lines.push(`### ${panel.title}`);
      for (const item of panel.items || []) lines.push(`- ${item}`);
      lines.push('');
    }
  }

  lines.push('## Source Trace');
  for (const section of report.sections) {
    lines.push(`### ${section.title}`);
    lines.push(`- ${section.finding}`);
    lines.push(`- Status: ${section.source.status}`);
    lines.push(`- Confidence: ${section.source.confidence}`);
    lines.push(`- Source URL: ${section.source.sourceUrl}`);
    lines.push(`- Query Used: ${section.source.queryUsed}`);
    lines.push(`- Queried At: ${section.source.queriedAt}`);
    if (section.source.data) lines.push(`- Data: ${JSON.stringify(section.source.data)}`);
    lines.push('');
  }

  if (report.nextSteps?.recommendations?.length) {
    lines.push('## Recommended Next Steps');
    for (const step of report.nextSteps.recommendations) lines.push(`- ${step}`);
    lines.push(report.nextSteps.disclaimer);
    lines.push('');
  }

  lines.push('## Disclaimer');
  lines.push(report.disclaimer);
  return lines.join('\n');
}

